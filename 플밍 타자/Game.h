int SleepTime = 0;
void Crab_Input()
{
	LAnswer = "A";
	RAnswer = "B";
	Crab_Answer(0);
	Crab_Answer(1);
	while (life > 0)//게임이 끝날 떄 까지 반복
	{
		char Input;
		char inputarr[10];
		int inputC = 0;//사용자가 입력을 얼마나 했는지 카운트하는 변수 초기화
		while (life > 0)//게임이 끝날 때 까지 반복
		{
			if (life > 0)Input = _getch();//목숨이 다 닳기 전까지 입력을 받아줌
			else break;//목숨이 다 닳으면 반복 끝내줌
			if (Input == 13)//만약 엔터를 누르면
			{
				for (int i = 40; i < 104; i++)
				{
					Print_Crab[27][i] = ' ';//입력을 출력해줬던 자리를 다 지우고
					PlyerAnswer.clear();
				}
				break;//입력을 끝내주고 입력받은 단어에 맞는 단어가 있는지 판별하러 가줌
			}
			else if (Input == '\b')//백스페이스를 누르면
			{
				inputC--;//입력 카운트를 하나 지워주고
				Print_Crab[27][49 + inputC] = ' ';//이전에 입력했던 좌표로 이동해 공백으로 지워줌
			}
			else//위 코드들에 전부 해당하지 않으면
			{
				Print_Crab[27][49 + inputC] = Input;//콘솔배열에 입력받은값을 넣어줘 입력 받은 걸 표시해줌
				inputarr[inputC] = Input;//입력받는 배열에 값을 넣어줌
				inputC++;//문자 하나를 입력 했으니 다음 문자를 다음 자리에 넣어주기 위해 입력 카운트에 1 더해줌
			}
		}
		for (int i = 0; i < inputC; i++)PlyerAnswer += inputarr[i];
		if (PlyerAnswer == RAnswer)
		{
			if (CrabX + 8 != 104)
			{
				CrabX++;
				SleepTime = 50;
				Print_Crab[22][CrabX - 1] = ' '; Print_Crab[23][CrabX - 1] = ' ', Print_Crab[24][CrabX] = ' ';
				SleepTime = 0;
			}
			Crab_Answer(1);
		}
		else  if (PlyerAnswer == LAnswer)
		{
			if (CrabX != 1)
			{
				CrabX--;
				SleepTime = 50;
				Print_Crab[22][CrabX + 8] = ' '; Print_Crab[23][CrabX + 8] = ' ', Print_Crab[24][CrabX + 7] = ' ';
				SleepTime = 0;
			}

			Crab_Answer(0);
		}
	}
}
void Poop_Down()
{
	while (life > 0)
	{
		for (int i = 0; i < PoopCount; i++)
		{
			if (Plist[i].y == 24)
			{
				Print_Crab[24][Plist[i].x] = ' ';
			}
			else
			{
				Print_Crab[Plist[i].y][Plist[i].x] = ' ';
				Plist[i].y++;
				if (Plist[i].x >= CrabX && Plist[i].x <= CrabX + 7 && Plist[i].y >= 22 && Plist[i].y <= 24)
				{
					Clife_over();
					Plist[i].y = 24;
				}
				else Print_Crab[Plist[i].y][Plist[i].x] = Plist[i].Pooop;
			}
		}
		Sleep(500);
	}
	return;
}
