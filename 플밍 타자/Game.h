void Poop_Gen(int level)
{
	while (PoopCount == 1000 || life > 0)//게임이 끝나거나 단어생성이 최대에 도달할 떄 까지 반복
	{
		RANDOM(0, 103);
		Plist[PoopCount].x = ++random;
		Plist[PoopCount].y = 1;//구조체에 y좌표를 넣어줌
		Print_Crab[Plist[PoopCount].y][Plist[PoopCount].x] = Plist[PoopCount].Pooop;
		PoopCount++;//구조체 배열에 단어가 하나 찼으니까 그 다음 배열애 새 단어를 또 넣어줄 수 있도록 단어 생성 카운트에 1을 더해줌
		Sleep(level * 500);//단어 생성을 기다려줌
	}
	return;
}
